"""Phase 28 generate-foundation — cross-seed config.js generator (INTENT-02 / XSEED-02).

Pure-function module: CrossSeedConfig → committed config.js content string.
This is the P28 D-03 proving slice: emit novel JS literal format from the
generate framework, de-risking Phase 30 (full cross-seed reconciler).

Key invariants:
- generate_cross_seed is a pure function (no I/O, no file access, no httpx).
- Determinism guaranteed by json.dumps(sort_keys=True): byte-identical across runs.
- Empty list fields (torznab, torrent_clients, link_dirs) are omitted from output.
- link_type and action are ALWAYS emitted (non-empty defaults).
- Security: json.dumps handles all value escaping — no f-string interpolation of
  raw user values (T-28-04 / T-28-05 STRIDE mitigations).
"""

from __future__ import annotations

import json
from typing import Final

from arrconf.intent_config import CrossSeedConfig

_HEADER: Final[str] = "// GENERATED by 'arrconf generate' from intent.yml — DO NOT EDIT BY HAND\n"


def generate_cross_seed(cfg: CrossSeedConfig) -> str:
    """Pure function: CrossSeedConfig → committed config.js content string.

    Determinism guarantee: json.dumps(sort_keys=True) produces byte-identical
    output across runs regardless of pydantic field insertion order.

    This is the P28 proving slice (D-03): validates that the generate framework
    supports JS literal emission (CommonJS module.exports), not just YAML.
    Phase 30 will wire the CLI generate command to write this to disk.

    Empty list fields are omitted from the JS object (operator intent: unset ≠
    empty array). linkType and action are always present (they carry defaults).
    """
    data: dict[str, object] = {}
    if cfg.torznab:
        data["torznab"] = cfg.torznab
    if cfg.torrent_clients:
        data["torrentClients"] = cfg.torrent_clients
    if cfg.link_dirs:
        data["linkDirs"] = cfg.link_dirs
    data["linkType"] = cfg.link_type
    data["action"] = cfg.action
    body = json.dumps(data, indent="\t", sort_keys=True, ensure_ascii=False)
    return f"{_HEADER}module.exports = {body};\n"
